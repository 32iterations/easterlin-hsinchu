def download_image(url, max_retries=2):
    """å¾URLä¸‹è¼‰åœ–ç‰‡"""
    for attempt in range(max_retries):
        try:
            response = requests.get(url, timeout=15)
            if response.status_code == 200:
                return BytesIO(response.content)
        except Exception as e:
            print(f"  âš ï¸  åœ–ç‰‡ä¸‹è¼‰å¤±æ•— (å˜—è©¦ {attempt+1}/{max_retries}): {e}")
    return None

def add_background_dark(slide):
    """æ·»åŠ æ·±è‰²èƒŒæ™¯"""
    background = slide.background
    fill = background.fill
    fill.solid()
    fill.fore_color.rgb = BG_DARK

def add_image_background(slide, image_url, opacity=0.5):
    """æ·»åŠ åœ–ç‰‡èƒŒæ™¯ä¸¦åŠ é®ç½©"""
    img_stream = download_image(image_url)
    if img_stream:
        try:
            pic = slide.shapes.add_picture(
                img_stream,
                Inches(0), Inches(0),
                width=Inches(10),
                height=Inches(5.625)
            )
            slide.shapes._spTree.remove(pic._element)
            slide.shapes._spTree.insert(2, pic._element)

            overlay = slide.shapes.add_shape(
                MSO_SHAPE.RECTANGLE,
                Inches(0), Inches(0),
                Inches(10), Inches(5.625)
            )
            overlay.fill.solid()
            overlay.fill.fore_color.rgb = BG_DARK
            overlay.fill.transparency = opacity
            overlay.line.fill.background()
            return True
        except Exception as e:
            print(f"  âŒ èƒŒæ™¯åœ–ç‰‡è¨­ç½®å¤±æ•—: {e}")
    return False

def add_title(slide, text, top=0.3, color=PRIMARY, size=40):
    """æ·»åŠ æ¨™é¡Œ"""
    box = slide.shapes.add_textbox(Inches(0.5), Inches(top), Inches(9), Inches(0.7))
    tf = box.text_frame
    tf.text = text
    p = tf.paragraphs[0]
    p.font.size = Pt(size)
    p.font.bold = True
    p.font.color.rgb = color
    p.alignment = PP_ALIGN.LEFT

def add_section_card(slide, left, top, width, height, title, content_list, color=PRIMARY):
    """æ·»åŠ å…§å®¹å¡ç‰‡"""
    # èƒŒæ™¯å¡ç‰‡
    card = slide.shapes.add_shape(
        MSO_SHAPE.ROUNDED_RECTANGLE,
        Inches(left), Inches(top),
        Inches(width), Inches(height)
    )
    card.fill.solid()
    card.fill.fore_color.rgb = BG_CARD
    card.fill.transparency = 0.1
    card.line.color.rgb = color
    card.line.width = Pt(2)

    # æ¨™é¡Œ
    title_box = slide.shapes.add_textbox(
        Inches(left + 0.15), Inches(top + 0.15),
        Inches(width - 0.3), Inches(0.35)
    )
    tf = title_box.text_frame
    tf.text = title
    p = tf.paragraphs[0]
    p.font.size = Pt(16)
    p.font.bold = True
    p.font.color.rgb = color

    # å…§å®¹
    content_box = slide.shapes.add_textbox(
        Inches(left + 0.15), Inches(top + 0.55),
        Inches(width - 0.3), Inches(height - 0.7)
    )
    tf = content_box.text_frame
    tf.word_wrap = True

    for item in content_list:
        p = tf.add_paragraph()
        p.text = item
        p.font.size = Pt(11)
        p.font.color.rgb = TEXT_WHITE
        p.space_before = Pt(4)

